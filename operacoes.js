//Decremento
console.log(--numero);

//Comparação de Maior
console.log(10 > 5);


//Comparação de Menor
console.log(10 < 25);

//Comparação de Maior ou Igual
console.log(10 >= 12);

//Comparação de Menor ou Igual
console.log(200 <= 200);

//Comparação de Igualdade
console.log( 100 == 101);

//Comparação de Igualdade Rígida
console.log(100 === "100");

//Comparação de Diferente
console.log(10 != 8);

//Comparação de Diferente Rígido
console.log(10 !== "10");

//Comparador lógico &&
console.log(10 <= 15 && 8 <= 10);

//Comparador lógico OR ||
console.log(10 >= 8 || 25 > 26);

//Comparador negação !
